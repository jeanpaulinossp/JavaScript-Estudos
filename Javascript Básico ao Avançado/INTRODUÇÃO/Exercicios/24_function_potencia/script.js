function calcularDoisNumeros (bc, expoente) {
    return Math.pow (bc, expoente);
}
    console.log (calcularDoisNumeros (2,2));
    console.log (calcularDoisNumeros (2,3));
    console.log (calcularDoisNumeros (2,4));


function potencia (a,b) {
    return a ** b; // Usar dois asteriscos também significa potência
}
    console.log (potencia (2,2));
    console.log (potencia (2,3));
    console.log (potencia (2,4));
