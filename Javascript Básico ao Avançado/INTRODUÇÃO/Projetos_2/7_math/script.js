let menor = Math.min (6,12,28,55,8);

console.log (`Os números apresentados são: 6,12,28,55,8 e o menor entre eles é o: ${menor} usando a função Math.min`);

let arredondar = Math.round (2.4969899);

console.log (`O número apresentado foi 2.4969899 e foi arrendondado para: ${arredondar} usando a função Math.round`);

